namespace NetAF.Targets.Markup
{
    /// <summary>
    /// Provides constants for markup.
    /// </summary>
    internal static class MarkupSyntax
    {
        #region Constants

        /// <summary>
        /// Get the character that denotes a heading.
        /// </summary>
        internal const char Heading = '#';

        /// <summary>
        /// Get the character that denotes a new line.
        /// </summary>
        internal const char NewLine = '\n';

        /// <summary>
        /// Get the character that denotes a space.
        /// </summary>
        internal const char Space = ' ';

        /// <summary>
        /// Get the character that denotes a tag opening.
        /// </summary>
        internal const char OpenTag = '[';

        /// <summary>
        /// Get the character that denotes a closing.
        /// </summary>
        internal const char CloseTag = ']';

        /// <summary>
        /// Get the character that denotes a ending tag.
        /// </summary>
        internal const char EndTag = '/';

        /// <summary>
        /// Get the string that denotes bold.
        /// </summary>
        internal const string Bold = "bold";

        /// <summary>
        /// Get the string that denotes italic.
        /// </summary>
        internal const string Italic = "italic";

        /// <summary>
        /// Get the string that denotes strikethrough.
        /// </summary>
        internal const string Strikethrough = "strikethrough";

        /// <summary>
        /// Get the string that denotes foreground.
        /// </summary>
        internal const string Background = "foreground";

        /// <summary>
        /// Get the string that denotes background.
        /// </summary>
        internal const string Foregound = "background";

        /// <summary>
        /// Get the string that denotes delimiter.
        /// </summary>
        internal const char Delimiter = ':';

        #endregion
    }
}
