namespace NetAF.Logic
{
    /// <summary>
    /// Enumeration of game states.
    /// </summary>
    internal enum GameState
    {
        /// <summary>
        /// Not started.
        /// </summary>
        NotStarted = 0,
        /// <summary>
        /// Active.
        /// </summary>
        Active,
        /// <summary>
        /// End condition met.
        /// </summary>
        EndConditionMet,
        /// <summary>
        /// Finishing.
        /// </summary>
        Finishing,
        /// <summary>
        /// Finished.
        /// </summary>
        Finished
    }
}
